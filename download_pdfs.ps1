$outDir = "c:\DesarrolloIA\Traiding\documentacion"
$downloaded = 0
$i = 1

foreach ($url in $urls) {
    if ($downloaded -ge 55) {
        break
    }
    
    $fileName = [System.IO.Path]::GetFileName([uri]$url.LocalPath)
    if ([string]::IsNullOrWhiteSpace($fileName) -or (-not $fileName.EndsWith(".pdf", "CurrentCultureIgnoreCase"))) {
        $fileName = "trading_document_$i.pdf"
    } else {
        # replace invalid chars just in case
        $fileName = [RegEx]::Replace($fileName, "[^\w\.-]", "_")
        # prefix with index to avoid overwriting
        $fileName = "${i}_${fileName}"
    }
    
    $outPath = Join-Path $outDir $fileName
    
    Write-Host "Downloading $url to $outPath"
    try {
        Invoke-WebRequest -Uri $url -OutFile $outPath -TimeoutSec 30 -ErrorAction Stop
        Write-Host "Success"
        $downloaded++
    } catch {
        Write-Host "Failed to download $url : $_"
    }
    $i++
}

Write-Host "Total successfully downloaded: $downloaded"
